int longpalind(string &str, int n){
   int L[n][n];
   for (int i = 0; i < n; i++)
      L[i][i] = 1;

    for (int l=2; l<=n; l++)
    {
        for (int i=0; i<n-l+1; i++)
        {
            int j = i+l-1;
            if (str[i] == str[j] && l == 2)
               L[i][j] = 2;
            else if (str[i] == str[j])
               L[i][j] = L[i+1][j-1] + 2;
            else
               L[i][j] = max(L[i][j-1], L[i+1][j]);
        }
    }

    return L[0][n-1];

}

